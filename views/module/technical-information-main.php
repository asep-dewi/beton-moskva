        <div class="accordion__head"><span>Эксплуатационные свойства</span></div>
        <div class="accordion__desc">
          <p>У бетона есть ряд эксплуатационных свойств, которые определяют его прочность, долговечность и устойчивость к воздействию различных факторов.</p>
          <ul>
            <li><strong>Прочность на сжатие:</strong> одно из основных свойств бетона и означает его способность выдерживать нагрузки при сжатии. Измеряется в единицах давления (например, мегапаскали - МПа). Обычно указывается в виде "Класс бетона по прочности" (например, В15, В20, В25 и т. д.), где число обозначает прочность в МПа. Чем выше значение, тем более прочный бетон.</li>

            <li><strong>Прочность на изгиб:</strong> свойство бетона определяет его способность выдерживать нагрузки, вызывающие изгиб. Бетон может использоваться в конструкциях, где необходимо сопротивление изгибу, например, в балках и плитах перекрытия. Прочность на изгиб обычно указывается в технической документации или нормативах.</li>

            <li><strong>Морозостойкость бетона:</strong> бетон должен обладать достаточной морозостойкостью, чтобы выдерживать периодическое замерзание и оттаивание. Когда вода в порах бетона замерзает, она расширяется, что может вызвать повреждение материала. Хорошая морозостойкость достигается путем правильного соотношения ингредиентов, использования добавок и контроля водопроницаемости.</li>

            <li><strong>Водонепроницаемость:</strong> способность бетона предотвращать проникновение воды через его структуру. Бетон, который не пропускает воду, защищает арматуру от коррозии и сохраняет свою прочность и долговечность. Для достижения хорошей водонепроницаемости могут применяться специальные добавки и гидроизоляционные меры.</li>

          </ul>
        </div>
