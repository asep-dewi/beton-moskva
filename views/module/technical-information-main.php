        <div class="accordion__head"><span>Защита</span></div>
        <div class="accordion__desc">

          <p>Гидроизоляционная защита бетона включает два типа мероприятий: первичную и вторичную. Первичная защита направлена на обеспечение непроницаемости бетонных конструкций. Она основывается на использовании материалов с повышенной коррозионной стойкостью в агрессивной среде и обеспечивает низкую проницаемость бетона. Кроме того, в рамках первичной защиты рассматривается выбор оптимальных форм и геометрических параметров конструкций, определение трещиностойкости и предельно допустимой ширины трещин, а также оценка сочетания нагрузок и определение временного раскрытия трещин. Толщина защитного слоя бетона также играет важную роль и определяется с учетом его непроницаемости. Помимо этого, первичная защита может включать применение интегральных капиллярных материалов, которые повышают водонепроницаемость, морозостойкость, прочность на сжатие и коррозионную стойкость бетона на протяжении всего срока службы.</p>

          <p>Задача вторичной защиты заключается в предотвращении контакта агрессивной среды с бетоном или его ограничении. Вторичная защита включает использование обеспыливающих пропиток, тонкослойных покрытий, наливных полов и высоконаполненных покрытий. При производстве полимерных составов, которые применяются в качестве связующего материала, чаще всего используются эпоксидные, полиуретановые и полиэфирные компоненты. Механизм защиты бетонной поверхности заключается в уплотнении поверхностного слоя и его изоляции.</p>

          <p>Особенно актуальной проблемой является защита бетона от химической и электрокоррозии на объектах железнодорожного транспорта, где возникают блуждающие токи утечки, сопровождающиеся агрессивным химическим воздействием.</p>
