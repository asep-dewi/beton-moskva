        <div class="accordion__head"><span>Подбор состава</span></div>
        <div class="accordion__desc">
          <p>Песок играет важнейшую роль в составе бетонной смеси, представляя собой одну из ключевых компонентов. Приготовление бетона наилучшим образом осуществляется с использованием природного песка среднего или крупного размера зерна. Размер песчинок и их соотношение с крупными заполнителями, такими как щебень или гравий в тяжелом бетоне, а также керамзит в легком бетоне, оказывают влияние на подвижность смеси и потребность в цементе. Более мелкий песок требует большего количества минеральных заполнителей и воды.


          </p>
          <p> Однако важно отметить, что при использовании природного песка имеется ограничение на содержание глины или глинистых частиц. Мельчайшие глинистые частицы оказывают сильное воздействие на прочность бетона. Даже незначительное количество таких частиц может значительно снизить прочностные характеристики. В случае отсутствия природного песка без глинистых примесей, существуют способы улучшения имеющегося песка путем следующих процедур: промывка песка, фракционирование песка в потоке воды, выделение необходимой фракции и смешивание песка, имеющегося на месте работ, с качественным привезенным песком.
          </p>
          <p> Таким образом, обработка песка является неотъемлемой частью процесса, позволяющей обеспечить качество и прочность бетона, особенно при отсутствии природного песка без глинистых примесей.</p>
