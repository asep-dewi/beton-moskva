        <div class="accordion__head"><span>Обозначения</span></div>
        <div class="accordion__desc">

          <p>Согласно ГОСТ 7473-2010, система обозначения бетонной смеси включает следующие элементы: тип бетонной смеси, класс прочности, марку удобоукладываемости, марку морозостойкости (при необходимости), марку водонепроницаемости (при необходимости), среднюю плотность (для лёгкого бетона) и обозначение стандарта.</p>

          <p>Например, если рассмотреть готовую к применению бетонную смесь тяжёлого бетона, обладающую классом прочности на сжатие В25, маркой удобоукладываемости П3, маркой морозостойкости F200 и маркой водонепроницаемости W6, то её обозначение в соответствии с ГОСТ 7473-2010 будет следующим: БСТ В25 П3 F200 W6 ГОСТ 7473-2010.</p>


          <p>В коммерческой практике также принято выделять в отдельную категорию высокопрочные спецбетоны, обозначаемые как ВС, а также бетоны, в которых используется щебень мелкой фракции, известные как бетоны СМ или "семечка".</p>


          <p>Эта система обозначения позволяет строителям и инженерам однозначно определить свойства и характеристики бетонной смеси, что облегчает выбор и использование подходящей смеси для конкретного строительного проекта.</p>
