          <p>
            В соответствии с ГОСТ 25192-2012 и ГОСТ 7473-2010 (ранее известным как 7473-94), бетоны подразделяются по своему основному назначению, типу связующего вещества, характеру заполнителей, структуре и условиям отверждения. </p>
          <ul class="list list__default">
            <li>Бетоны делятся на две основные категории: обычные, применяемые в промышленном и гражданском строительстве, и специальные - такие как гидротехнические, дорожные, теплоизоляционные, декоративные. Также существуют бетоны специального назначения, включающие химически стойкие, жаростойкие, звукопоглощающие, а также используемые для защиты от ядерного излучения и других целей.</li>
            <li>Существует несколько видов вяжущих веществ: цементные, силикатные, гипсовые, шлакощелочные, асфальтобетон, пластобетон (полимербетон) и другие. Эти материалы различаются по своему составу и свойствам, предназначены для разных строительных задач.</li>
            <li>В бетоне используются различные заполнители, которые отличаются использованием плотных, пористых или специальных материалов в качестве заполнителей.</li>
            <li>Бетон классифицируется на разные типы твердения: естественное, тепловлажностное при атмосферном давлении и тепловлажностное при давлении выше атмосферного (автоклавное твердение).
              Таким образом, каждый из этих видов бетона обладает своими уникальными свойствами и адаптирован к разных условиях. </li>
