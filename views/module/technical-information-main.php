      <div class="accordion" data-accordion>
        <div class="accordion__head"><span>Удобоукладываемость</span></div>
        <div class="accordion__desc">

          <p>Эксплуатационные свойства бетона, такие как удобоукладываемость, могут значительно изменяться в зависимости от состава и свойств самого бетона. Вот несколько основных типов бетонов и их характеристики относительно удобоукладываемости:</p>

          <ul>
            <li><strong>Жидкий (пластичный) бетон:</strong> обладает высокой удобоукладываемостью и может быть легко распределен по форме без необходимости дополнительных усилий. Это позволяет получить гладкую и равномерную поверхность. Он часто используется при заливке труднодоступных мест или при создании конструкций с комплексной геометрией.</li>
            <li><strong>Подвижный (пластичный) бетон:</strong> также обладает хорошей удобоукладываемостью, но он более консистентен и имеет немного более высокую вязкость по сравнению с жидким бетоном. Он позволяет легко и равномерно распределиться вокруг арматуры и других вставок, обеспечивая хорошую адгезию и прочность конструкции.</li>
            <li><strong>Жёсткий (некогерентный) бетон:</strong> имеет низкую удобоукладываемость и требует дополнительных усилий для его распределения. Он обычно используется в случаях, когда требуется создание более прочной и плотной структуры, такой как фундаменты или дорожные покрытия. В процессе укладки жёсткого бетона могут применяться специальные инструменты и техники для обеспечения правильного распределения и уплотнения.</li>
            <li><strong>Сверхжёсткий (самоуплотняющийся) бетон:</strong> обладает уникальной способностью распределиться и заполнить форму без вибрации или других механических усилий. Он содержит специальные добавки, которые обеспечивают высокую подвижность и самоуплотняющиеся свойства. Это упрощает процесс укладки, повышает качество поверхности и уменьшает возможность образования пустот или трещин.</li>
          </ul>
